/**
* @brief This function handles TIM6 global interrupt and DAC1, DAC2 underrun error interrupts.
*/
void TIM6_DAC_IRQHandler(void)
{
  /* USER CODE BEGIN TIM6_DAC_IRQn 0 */
  LL_TIM_DisableIT_UPDATE(TIM6);
  LL_TIM_ClearFlag_UPDATE(TIM6);
  
  if(Tach_TimeTick < 10000){
    Tach_TimeTick++;
  }else{
    Tach_TimeBuf_Ave = 0;
    for(int i=1; i<TACH_AVE_NUMBER; i++){
      Tach_TimeBuf_Ave = Tach_TimeBuf_Ave + Tach_TimeBuf[i] - Tach_TimeBuf[i-1];
    }
    Tach_TimeBuf_Ave = Tach_TimeBuf_Ave / (TACH_AVE_NUMBER - 1);
    
    if(Tach_TimeBuf_Ave != 0){
      Tach_Value = 3000000 / Tach_TimeBuf_Ave;
    }else{
      Tach_Value = 0;
    }
    
    /* Trasport USART2 */
    sprintf(USART_Tx_Array, "%d\n\r", Tach_Value);
    if(Tach_Value / 10000 >= 1){
      USART_Tx_Size = 7;
    }else if(Tach_Value / 1000 >= 1){
      USART_Tx_Size = 6;
    }else{
      USART_Tx_Size = 5;
    }
    USART2_TxStart();
    
    Tach_TimeBuf_AveCnt = 0;
    Tach_TimeTick = 0;
  }
  /* USER CODE END TIM6_DAC_IRQn 0 */
  
  /* USER CODE BEGIN TIM6_DAC_IRQn 1 */
  LL_TIM_EnableIT_UPDATE(TIM6);
  /* USER CODE END TIM6_DAC_IRQn 1 */
}

